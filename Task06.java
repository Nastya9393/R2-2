public class Task06 {
	public static void main(String[] args) {
		final int w = 12;
		final int h = 3;
		
		int length1 = 2 * w + 2 * h; // периметр прямоугольника
		int length2 = w * h; // площадь прямоугольника
		
		System.out.println (length1);
		System.out.println (length2);
	}

}
