package Firstlesson;

public class Task05 {
	public static void main(String[] args) {
		
		final double Pi = 3.1415926536;
		final int R = 5;
		
		double length1 = 2 * Pi * R; // длина окружности
		double length2 = Pi * R * R; // площадь круга
		
		System.out.println (length1);
		System.out.println (length2);
		
	}

}
